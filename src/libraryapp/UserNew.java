    public void loadCBItems(){
        Member placeholder = new Member(-1, "---Please select---", "", "", "", "", 1);
        DefaultComboBoxModel<Member> model = new DefaultComboBoxModel<>();
        model.addElement(placeholder);
        
        for (Member m : this.memberDAOImpl.viewAllSorted()) {
            model.addElement(m);
        }
        
        cbMemberId.setModel(model);
    }
    
