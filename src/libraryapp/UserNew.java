    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int option = JOptionPane.showConfirmDialog(null, "Are you sure?", "System Confirmation!", JOptionPane.OK_CANCEL_OPTION);
        
        if(option != JOptionPane.OK_OPTION){
            return;
        }
        
        InputValidator inputValidator = new InputValidator();
        String username = tfUsername.getText();
        String password = tfPassword.getText();
        
        String errorMsg = "";
        
        if(!inputValidator.validateString(username, 3)){
            errorMsg += "- Username must be at least 3 characters long.\n";
        }  
        
        if(!inputValidator.validateString(password, 8)){
            errorMsg += "- Password must be at least 8 characters long.\n";
        }
        
        int memberId = -1;
        Member ms = (Member) cbMemberId.getSelectedItem();
        
        if (ms != null) {
            memberId = ms.getId(); 
        }
        
        if(memberId < 0){
            errorMsg += "- Member Name must be selected.\n";
        } 
        
        int role = 0;
        
        if(rbtnAdmin.isSelected()){
            role = 1;
        } 
        
        if (btngRole.getSelection() == null) {
            errorMsg += "- Please select a role.\n";
        }

        if (errorMsg.length() > 0) {
            JOptionPane.showMessageDialog(this, "Please correct the following errors:\n" + errorMsg.toString(), "Validation Error", JOptionPane.ERROR_MESSAGE);
            return; 
        }
        
        try {
            this.user = new User(0, memberId, username, password, role, 1);
            this.confirmed = true;
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Fatal Error: Numeric data is too large for the system.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

