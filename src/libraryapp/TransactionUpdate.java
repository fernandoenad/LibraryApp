    public void populateForm(){
        tfId.setText(String.format("%s",this.transaction.getId()));
        
        for(int i=0; i<this.members.size(); i++){
            if(this.transaction.getMember_id() == this.members.get(i).getId()){
                tfBorrower.setText(String.format("%s", this.members.get(i).getName()));
                break;
            }
        }
        
        for (int i = 0; i < cbBook_id.getItemCount(); i++) {            
            Book bi = (Book) cbBook_id.getItemAt(i);
            System.out.println(bi.getId());
            System.out.println(this.transaction.getBook_id());
            if (bi.getId() == this.transaction.getBook_id()) {
                cbBook_id.setSelectedIndex(i);
                break;
            }
        }
        
        tfDateBorrowed.setText(String.format("%s",this.transaction.getDateBorrowed()));
        tfDateDue.setText(String.format("%s",this.transaction.getDateDue()));

        if(this.transaction.getStatus() == 1){
            rbtnReturned.setSelected(true);
        } else {
            rbtnLoaned.setSelected(true);
        }
    }
