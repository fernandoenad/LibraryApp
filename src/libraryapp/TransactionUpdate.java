    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int option = JOptionPane.showConfirmDialog(null, "Are you sure?", "System Confirmation!", JOptionPane.OK_CANCEL_OPTION);
        
        if(option != JOptionPane.OK_OPTION){
            return;
        }
        
        InputValidator inputValidator = new InputValidator();
        String dateBorrowed = tfDateBorrowed.getText();
        String dateDue = tfDateDue.getText();
        
        String errorMsg = "";
        
        if(!inputValidator.validateDate(dateBorrowed)){
            errorMsg += "- Date Borrowed should follow yyyy-mm-dd format.\n";
        }  
        
        if(!inputValidator.validateDate(dateDue)){
            errorMsg += "- Date Due should follow yyyy-mm-dd format.\n";
        }
       
        int book_id = -1;
        Book bs = (Book) cbBook_id.getSelectedItem();
        
        if (bs != null) {
            book_id = bs.getId(); 
        }
        
        if(book_id < 0){
            errorMsg += "- Book Title must be selected.\n";
        } 
        
        int status = 0;
        
        if(rbtnReturned.isSelected()){
            status = 1;
        }

        if (errorMsg.length() > 0) {
            JOptionPane.showMessageDialog(this, "Please correct the following errors:\n" + errorMsg.toString(), "Validation Error", JOptionPane.ERROR_MESSAGE);
            return; 
        }
        
        try {
            this.transaction = new Transaction(this.transaction.getId(), this.transaction.getMember_id(), book_id, dateBorrowed, dateDue, status);
            this.confirmed = true;
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Fatal Error: Numeric data is too large for the system.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

